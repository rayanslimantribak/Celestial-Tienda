let idiomaActual = localStorage.getItem('celestial_idioma') || 'es';

function cambiarIdioma(idioma) {
    if (traducciones[idioma]) {
        idiomaActual = idioma;
        localStorage.setItem('celestial_idioma', idioma);
        aplicarTraducciones();
    }
}

function aplicarTraducciones() {
    const t = traducciones[idiomaActual];
    document.querySelectorAll('[data-key]').forEach(elemento => {
        const key = elemento.getAttribute('data-key');
        if (t[key]) {
            elemento.textContent = t[key];
        }
    });
}

